def bfs(maze):

    visited=set()
    vis_d={}
    queued=[start]
    path=[]
    x=0
    y=0

    while True:
        
        try:
            l=queued.pop(0)
        except:
            print("END NOT REACHABLE")
            con=False
            break
        

        x=l[0]
        y=l[1]

        if (maze[x][y]=='E'):
            visited.add((x, y))
            break

        if tuple(l) in visited:
            continue
        
        visited.add(tuple(l))
        
        neighbour=[
            [1,0],
            [0,1],
            [-1,0],
            [0,-1]
        ]

        for i in neighbour:

            x_new=x+i[0]
            y_new=y+i[1]
        
            if 0<=x_new<len(maze) and 0<=y_new<len(maze[0]):
                if((x_new, y_new) not in visited and
                [x_new,y_new] not in queued and
                maze[x_new][y_new]!=1
                ):
                    queued.append([x_new,y_new])
                    vis_d[(x_new,y_new)] = (x,y)
                    maze_printing(visited)
                    os.system('cls')
                  
                    
    
    end = (x, y)
    current = end

    while current != (start[0], start[1]):
        path.append(current)
        current = vis_d[current]

    path.append((start[0], start[1]))
    path.reverse()
    #print("\nPATH : ",path)

    return path

